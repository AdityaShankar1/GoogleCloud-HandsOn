# 1. Read from the Silver table we just created
silver_df = spark.read.format("bigquery").load(f"{PROJECT_ID}.spotify_analysis.tracks_silver")

# 2. Aggregate metrics by Genre
genre_metrics = silver_df.groupBy("genre") \
    .agg(
        {"danceability": "avg", "energy": "avg", "track_name": "count"}
    ) \
    .withColumnRenamed("avg(danceability)", "avg_dance") \
    .withColumnRenamed("avg(energy)", "avg_energy") \
    .withColumnRenamed("count(track_name)", "total_tracks") \
    .filter("total_tracks > 50") \
    .orderBy(col("avg_energy").desc())

print("--- GENRE VIBE CHECK ---")
genre_metrics.show(10)