from pyspark.sql.functions import col, expr

# 1. Reference the existing table
raw_df = spark.read.format("bigquery").load(f"{PROJECT_ID}.spotify_analysis.tracks_raw")

# 2. Clean and Filter using try_cast (the safe way)
# This converts '0.85' -> 0.85 and 'german' -> NULL
cleaned_top_tracks = raw_df.select(
    "track_name",
    "artist_name",
    expr("try_cast(danceability AS double)").alias("danceability"),
    expr("try_cast(energy AS double)").alias("energy"),
    expr("try_cast(popularity AS double)").alias("popularity"),
    "genre"
).filter(
    (col("danceability") > 0.8) &
    (col("energy") > 0.8) &
    (col("popularity").isNotNull()) # This naturally drops the shifted rows
).orderBy(col("popularity").desc())

# 3. Final Result
print("Top 10 High-Energy Dance Tracks (Cleaned):")
cleaned_top_tracks.show(10)

# 1. Ensure the table name is defined
TARGET_SILVER_TABLE = f"{PROJECT_ID}.spotify_analysis.tracks_silver"

